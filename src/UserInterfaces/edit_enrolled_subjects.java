    //Function to check if current date is 4 weeks before the starting date of the semester given
    public boolean dateValidationForSubjectSelectionEditting(String date1,String date2) throws SQLException {
        try{
            Date d1 = new SimpleDateFormat("yyyy-MM-dd").parse(date1);
            Date d2 = new SimpleDateFormat("yyyy-MM-dd").parse(date2);
            System.out.println(d1+" "+d2);
            GregorianCalendar c1 = new GregorianCalendar(); 
            c1.setTime(d1); 
            GregorianCalendar c2 = new GregorianCalendar(); 
            c2.setTime(d2); 
            for( long i=1; ; i++ ) { 
                    c1.add( Calendar.WEEK_OF_YEAR, 1 ); // add one day, week, year, etc. 
            if( c1.after(c2) ) 
                    return (int)i>=4;
            }
        }catch(ParseException e){
            System.out.println("Oops");
            return false;
        }
    } 
    private void confirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmButtonActionPerformed
        String student_id = getStudentID.getText();    
        String academic_year = (String) academicYearCombo.getSelectedItem();
        String semester_number = semesterNumber.getSelection().getActionCommand();
        int year;
        if (null == academic_year)
        year = 4;
        else switch (academic_year) {
            case "1st Year":
            year = 1;
            break;
            case "2nd Year":
            year = 2;
            break;
            case "3rd Year":
            year = 3;
            break;
            default:
            year = 4;
            break;
        }
        semester sem = new semester(year, Integer.parseInt(semester_number));
        try {
            ResultSet rs = undergraduateController.checkStudentID(student_id);
            if (rs.next()) {
                semesterid = semesterController.findSemesterID(sem);
                ArrayList<String> list = studentSubjectController.getSelectedSubjectList(student_id, semesterid);
                if (list.isEmpty()){
                    JOptionPane.showMessageDialog(this, "Not Enrolled");
                    getStudentID.setText(null);
                }
                else{
                    //Checking if the current date is 4 weeks before the semester start date
                    String currentDate = new SimpleDateFormat("yyyy-MM-dd").format(Calendar.getInstance().getTime());
                    String sem_start_date = semesterController.getSemesterStartDate("SCS",semesterid);
                    if(dateValidationForSubjectSelectionEditting(currentDate,sem_start_date)){
                        JOptionPane.showMessageDialog(this, "Allowed to Edit");
                        
                         //Setting the subjects to be selected from
                        ArrayList<String> selectionSubjectList = subjectController.getSubjects("SCS",semesterid,false);
                        for (String subject : selectionSubjectList) {
                            selectionSubjectListModel.addElement(subject);
                        }
                        subjectList.setModel(selectionSubjectListModel);

                        getSemester.setVisible(false);
                        editSelectionPanel.setVisible(true);
                        studentID.setEditable(false);
                        studentID.setText(student_id);
                        semesterID.setEditable(false);
                        semesterID.setText(Integer.toString(semesterid));
                        for (String subject : list) {
                            selectedSubjectListModel.addElement(subject);
                            selectionSubjectListModel.remove(selectionSubjectListModel.indexOf(subject));
                            subjectList.setModel(selectionSubjectListModel);
                        }
                        subjectSelectedList.setModel(selectedSubjectListModel);
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "Not Allowed to Edit! Redirecting to Homepage");
                        new homepage().setVisible(true);
                        this.dispose();
                    }
                }
            }else{
                JOptionPane.showMessageDialog(this, "Student Not Found");
                getStudentID.setText(null);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }//GEN-LAST:event_confirmButtonActionPerformed

