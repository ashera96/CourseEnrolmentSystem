    private void addSem1SubjectButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addSem1SubjectButtonActionPerformed
        if(subjectList.isSelectionEmpty()) {
            JOptionPane.showMessageDialog(this,"No Subject Selected");
        }
        else {
            if(selectedSubjectListModel.size()<4) {
                selectedSubjectListModel.addElement(subjectList.getSelectedValue());
                selectionSubjectListModel.remove(subjectList.getSelectedIndex());
                subjectList.setModel(selectionSubjectListModel);
                subjectSelectedList.setModel(selectedSubjectListModel);
            }
            else {
                JOptionPane.showMessageDialog(this, "Subject Limit Reached");
            }
        }
    }//GEN-LAST:event_addSem1SubjectButtonActionPerformed

