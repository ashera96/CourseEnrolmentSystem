    private void doneButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneButtonActionPerformed
        String ID = getStudentID.getText();
        
        try{
            int result = studentSubjectController.deleteSelection(ID, semesterid);
            if ( result>0 ) {
                System.out.println("Deletion Successful");
            }else {
                System.out.println("Deletion Unsuccessful");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
        
        int size = subjectSelectedList.getModel().getSize();
        ArrayList<String> newlySelectedList = new ArrayList<>();
        
        try {
            newlySelectedList = subjectController.getCompulsorySubjects("SCS",semesterid,true);
            for (String compulsorySubject:newlySelectedList) {
                student_subject_semester addsubject = new student_subject_semester(ID,compulsorySubject,semesterid);
                studentSubjectController.addStudentSubject(addsubject);
            }
        } catch (SQLException ex) {
            Logger.getLogger(subject_selection.class.getName()).log(Level.SEVERE, null, ex);
        }
        for (int i=0;i<size;i++) {
            String subject_code;
            try {
                subject_code = subjectController.getSubjectCode(subjectSelectedList.getModel().getElementAt(i));
                student_subject_semester addsubject = new student_subject_semester(ID,subject_code,semesterid);
                studentSubjectController.addStudentSubject(addsubject);
            } catch (SQLException ex) {
                Logger.getLogger(subject_selection.class.getName()).log(Level.SEVERE, null, ex);
            }  
        }  
        JOptionPane.showMessageDialog(this,"Successfully Updated Subjects");
        new homepage().setVisible(true);
        this.dispose();
        

    }//GEN-LAST:event_doneButtonActionPerformed

