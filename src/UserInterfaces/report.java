    private void generateReportButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateReportButtonActionPerformed
        DefaultTableModel model = (DefaultTableModel) resultsTable.getModel();
        int totalCredits=0;
        double GPA=0;
        String student_id = studentID.getText();
        int semester_id = Integer.parseInt(semesterID.getText());

        try{
            ResultSet studentCheck = undergraduateController.checkStudentID(student_id);
            if (studentCheck.next()){
                generateReportButton.setEnabled(false);
                studentID.setEditable(false);
                semesterID.setEditable(false);
                ArrayList<String> subjectCodeList = studentSubjectController.getFollowingSubjectCodeList(student_id, semester_id);
                for (String subj : subjectCodeList) {
                    double totalMark = 0;
                    int examMark = assignmentController.getExamMark(student_id,subj);// Exam weight is 50
                    totalMark+=examMark*0.5;
                    ArrayList<mark> assignmentMarks = assignmentController.getMarkWeight(student_id, subj);
                    for (mark temp : assignmentMarks) {
                        totalMark+=temp.getMark()*temp.getWeight()/100.0;
                    }
                    
                    //Assigning the grade to the final mark obtained for the subject
                    String grade = findGrade(totalMark);
                    
                    String subject_name = subjectController.getSubjectName(subj);  
                    model.addRow(new Object[]{subject_name,grade});
                    emailMsgTxt+=subject_name+"\t\t:\t"+grade+"\n";
                    
                    //Calculating the number of credits for the subject
                    int tempCredit = subjectController.getCredits(subj);
                    totalCredits+=tempCredit;
                    GPA+=findGPV(totalMark)*tempCredit;
                }
                GPA/=totalCredits;
                gpa.setText(Double.toString(GPA));
                emailMsgTxt+="\nNumber of credits covered : "+totalCredits+"\nSemester GPA : "+GPA;
            }
            else{
                JOptionPane.showMessageDialog(this, "Student Not Found");
                studentID.setText(null);
                semesterID.setText(null);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }//GEN-LAST:event_generateReportButtonActionPerformed

    private void sendEmailButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendEmailButtonActionPerformed
//        emailMsgTxt = "Testing the content in email";
        emailSubjectTxt = "Overview of Index Number : "+studentID.getText();
        emailFromAddress = mailController.SMTP_AUTH_USER;

        // Add List of Email address to who email needs to be sent to
        String emailReceipeint[] = new String[1];
        try {
            emailList[0] = undergraduateController.getStudentEmailAddress(studentID.getText());
            emailReceipeint[0] = emailList[0];
        } catch (SQLException ex) {
            Logger.getLogger(report.class.getName()).log(Level.SEVERE, null, ex);
        }
              
        mailController smtpMailSender = new mailController();
        try {
            smtpMailSender.postMail(emailReceipeint, emailSubjectTxt, emailMsgTxt, emailFromAddress);
            JOptionPane.showMessageDialog(this, "Sucessfully Sent Mail");
        } catch (MessagingException ex) {
            Logger.getLogger(report.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_sendEmailButtonActionPerformed

