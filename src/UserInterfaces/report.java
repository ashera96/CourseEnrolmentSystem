    private String findGrade(double marks){
        if (marks>=90)
            return "A+";
        else if (marks>=80)
            return "A";
        else if (marks>=75)
            return "A-";
        else if (marks>=70)
            return "B+";
        else if (marks>=65)
            return "B";
        else if (marks>=60)
            return "B-";
        else if (marks>=55)
            return "C+";
        else if (marks>=50)
            return "C";
        else if (marks>=45)
            return "C-";
        else
            return "F";
    }
    
    private double findGPV(double marks){
        if (marks>=90)
            return 4.25;
        else if (marks>=80)
            return 4.00;
        else if (marks>=75)
            return 3.75;
        else if (marks>=70)
            return 3.25;
        else if (marks>=65)
            return 3.00;
        else if (marks>=60)
            return 2.75;
        else if (marks>=55)
            return 2.25;
        else if (marks>=50)
            return 2.00;
        else if (marks>=45)
            return 1.75;
        else
            return 0.00;
    }
    
