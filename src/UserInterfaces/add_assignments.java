    private void doneButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneButtonActionPerformed
        DefaultTableModel model = (DefaultTableModel) assignmentsTable.getModel();
        for (int i=0;i<assignmentsTable.getRowCount();i++){
            assignment Assignment = new assignment(model.getValueAt(i, 0).toString(),model.getValueAt(i, 1).toString(),model.getValueAt(i, 2).hashCode());
            try{
                int result = assignmentController.addAssignment(Assignment);
                if ( result<=0 )
                    JOptionPane.showMessageDialog(this,"Error");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
        }
        model.setRowCount(0);
        JOptionPane.showMessageDialog(this, "Successfully Updated Assignments");
        new homepage().setVisible(true);
        this.dispose();
    }//GEN-LAST:event_doneButtonActionPerformed

