    private void addAssignmentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addAssignmentButtonActionPerformed
        DefaultTableModel model = (DefaultTableModel) assignmentsTable.getModel();
        String subject_code = subjectCode.getText();
        String assignment_type = assignmentType.getText();
        int weight = Integer.parseInt(assignmentWeight.getText());
        
        try{
            subject subjectCheck = subjectController.searchSubject(subject_code);
            if (subjectCheck!=null){
                model.addRow(new Object[]{subject_code,assignment_type,weight});
            }
            else{
                JOptionPane.showMessageDialog(this, "Invalid Subject Code");
                subjectCode.setText(null);
                assignmentType.setText(null);
                assignmentWeight.setText(null);
            }
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }//GEN-LAST:event_addAssignmentButtonActionPerformed

    private void removeAssignmentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeAssignmentButtonActionPerformed
        DefaultTableModel model = (DefaultTableModel) assignmentsTable.getModel();
        if(assignmentsTable.getSelectedRow()==-1){
            if(assignmentsTable.getRowCount()==0)
                JOptionPane.showMessageDialog(this, "Table is Empty");
            else
                JOptionPane.showMessageDialog(this, "Please Select a Row");
        }
        else{
            model.removeRow(assignmentsTable.getSelectedRow());
        }
    }//GEN-LAST:event_removeAssignmentButtonActionPerformed

